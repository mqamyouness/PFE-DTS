Create DATABASE PFE
GO
USE PFE
go 
Create Table Personne(
	id int primary key identity,
	Nom varchar(255),
	Prenom varchar(255),
	Sexe varchar(255),
	DateNc Date,
	Telephone varchar(255),
	Email varchar(255),
	Username  varchar(255),
	MotsdePass varchar(255),
	Photoprofile varchar(255),
	ValidationChomp bit,
	TextValidation varchar(255),
	Argent Money
)
go
Create Table Carte(
	id int primary key identity,
	AdresseWeb varchar(255),
	NumeroCarte varchar(255),
	DateFin Date,
	CVC varchar(3),
	#idPersonne int foreign key references Personne(id)
) 
go
Create Table Utilisateur(
	id int primary key identity,
	#idPersonne  int foreign key references Personne(id)
)
go
Create Table Entraineur(
	id int primary key identity,
	Evaluation int,
	Experience int,
	DateDub Date,
	#idPersonne int foreign key references Personne(id)
)
go
Create Table LinkCertification(
	id int primary key identity,
	Link varchar(255),
	TextCRF Text,
	#idEntraineur int foreign key references Entraineur(id)
)
go
Create Table Paiement(
	id int primary key identity,
	Montant Money,
	Datep DateTime,
	#idEntraineur int foreign key references Entraineur(id)
)
go
Create Table MessageEnv(
	id int primary key identity,
	DateEnv DateTime,
	TextMessage Text,
	Lnik varchar(255)
)
go
Create Table EnvoyePar(
	#idEntraineur int foreign key references Entraineur(id),
	#idUtilisateur int foreign key references Utilisateur(id),
	#idMessage  int foreign key references MessageEnv(id),
	Deriction  bit,
	Constraint Pk primary key (#idEntraineur,#idUtilisateur,#idMessage)
)
go
Create Table Sport(
	id int primary key identity,
	Libelle varchar(255)
)
go
Create Table Choix(
	id int Primary key identity,
	#idEntraineur  int foreign key references Entraineur(id),
	#idSport  int foreign key references Sport(id),
	Category varchar(255),
	TypeEntrainment varchar(255),
	AbonnemetPrix Money,
	Constraint special UNIQUE (#idEntraineur,#idSport)
)
go
Create Table Abonnement(
	#idChoisez  int foreign key references Choix(id),
	#idUtilisateur  int foreign key references Utilisateur(id),
	DateDub Date,
	DateFin Date,
	Peroide int,
	autoReview  bit,
	Constraint Pk_A primary key (#idChoisez,#idUtilisateur)
)
go
Create Table Post(
	id int primary key identity,
	DatePost DateTime,
	Titer varchar(255)not null,
	Subjects varchar(255),
	TextPost Text,
	#idChoisez  int foreign key references Choix(id)
)
go
Create Table LinkPost(
	id int primary key identity,
	Link varchar(255),
	TextLinkPost Text,
	#idPost  int foreign key references Post(id)
)
go
Create Table Evaluation(
	#idPost  int foreign key references Post(id),
	#idPersonne  int foreign key references Personne(id),
	Evaluation  bit,
	Constraint Pk_E primary key (#idPost,#idPersonne)
)
go
Create Table Commentaire(
	id int primary key identity,
	DateComnt DateTime,
	TextComnt Text,
	#idPost  int foreign key references Post(id),
	#idPersonne  int foreign key references Personne(id)
)
go
Create Table Reponse(
	id int primary key identity,
	dateRpnd DateTime,
	TextRpnd Text,
	#idCommentaire  int foreign key references Commentaire(id),
	#idPesonne  int foreign key references Personne(id)
)
go
Create Table WhatPeopleSay(
	id int primary key identity,
	TextSay Text,
	show bit,
	#idPesonne  int foreign key references Personne(id)

)
go
Create table notifications (
	id int primary key identity,
	#idPersonne int foreign key references Personne(id),
	#idPost int foreign key references Post(id),
	Vue bit,
	typeNtif varchar(255)

)
go
Create table reseauxSociaux (
	id int primary key identity,
	Lnik varchar(255),
	nameRs varchar(255),
	#idPersonne int foreign key references Personne(id)
)
go
